void main() {
  //const инициализируется в момент компиляций
  const a = 'Const Hello World';
  //final инициализируется в момент запуска программы
  final b = DateTime.now();
  print(b);
//dynamic объявление переменной без указания типа.
  dynamic like = 99999;
  like = 1000;
}

// Константами const могут быть только заранее известные значения
